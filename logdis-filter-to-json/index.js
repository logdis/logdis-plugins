"use strict"

// --------------------------------------------------------------------------------------------------------------------

function logdisFilterToJson(opts) {
  opts = opts || {}

  opts.stuck = opts.stuck || process.stderr

  return function(data) {
    try {
      data = JSON.parse(data)
    }
    catch(err) {
      process.stderr.write('logdis-filter-to-json: Error:' + err + '\n')
      return
    }
    return data
  }
}

// --------------------------------------------------------------------------------------------------------------------

module.exports = logdisFilterToJson

